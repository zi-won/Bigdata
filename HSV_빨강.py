import cv2
src = cv2.imread('./data/tomato.jpg', cv2.IMREAD_COLOR)
hsv = cv2.cvtColor(src, cv2.COLOR_BGR2HSV)
lower_red1 = (8, 50, 50)
upper_red1 = (20, 255, 255)
lower_red2 = (100, 50, 50)
upper_red2 = (140, 255, 255)
mask_red1 = cv2.inRange(hsv, lower_red1, upper_red1)
mask_red2 = cv2.inRange(hsv, lower_red2, upper_red2)
mask_combined = cv2.bitwise_or(mask_red1, mask_red2)
red = cv2.bitwise_and(src, src, mask = mask_combined)
cv2.imshow('red', red)
cv2.waitKey(0)
cv2.destroyAllWindows()